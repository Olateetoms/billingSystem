def draw_grid(pen, grid):
    top= 230
    left= -100

    colors = ["black", "lightgreen", "blue", "orange", "yellow", "green", "red", "purple"] 
    
    for y in range(len(grid)):
        for x in range(len(grid[0])):
            screen_x = left + (x * 20)
            screen_y = top - (y *20)
            color_number = grid[y][x]
            color = colors[color_number]
            pen.color(color)
            pen.goto(screen_x, screen_y)
            pen.stamp()
                
draw_grid(pen, grid)








